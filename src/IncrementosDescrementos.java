public class IncrementosDescrementos {
    public static void main(String[] args) {
        //Operadores de Incremento y Decremento
        int count = 0;
        count++; //Incremanta en 1 despues de pasar esta linea
        count+=5; //Incremanta en 5
        ++count; //Incremanta en 1 antes de pasa esta linea
        System.out.println("count = " + count);



        int count1 = 10;
        count1--; //Decrementa en 1 despues de pasar esta linea
        count1-=5; //Decrementa en 5
        --count1; //Decrementa en 1 antes de pasa esta linea
        System.out.println("count = " + count1);
    }
}

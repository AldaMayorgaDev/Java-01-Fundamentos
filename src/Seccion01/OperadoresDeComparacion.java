package Seccion01;

public class OperadoresDeComparacion {
    public static void main(String[] args) {

        //Seccion01.Operadores de Comparacion

        double sueldo1 = 2000.56d;
        double sueldo2 = 4000.53d;

        System.out.println(sueldo1>sueldo2);
        System.out.println(sueldo1<sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1<=sueldo2);
        System.out.println(sueldo1!=sueldo2);
        System.out.println(sueldo1==sueldo2);
    }
}

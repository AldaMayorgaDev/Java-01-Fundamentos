class TiposDeDatos {
    public static void main(String[] args) {

        //Varaibles

        //TipoDato nombreVariable = valor
        int edad = 18;
        System.out.println("Edad"+edad);


        //Constantes
        //final TipoDato nombreVariable = valor
        final int edadAlumno = 18;
        System.out.println("Edad Alumno: "+edadAlumno);


        //Tipos de Datos
        /*
        * boolean
        * byte
        * short
        * char
        * int
        * float
        * long
        * double
        * */

        boolean isActive = false;
        byte tamanio = 50;
        short distancia = 4000;
        float sueldo = 30000f;
        double superficie = 300000000d;
        char inicialApellido = 'M';

    }
}
